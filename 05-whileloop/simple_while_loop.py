# count 0 -9
count = 1
while count <= 5:
    #Loop Body
    print('----- Round',count,'-----')
    print('Count = ',count)
    print('loop condition: ',count,' <= 5 ->', count <= 5)
    print('Count increases from ', count, ' to ', count +1)
    count +=1
    print('loop condition next round: ',count,' <= 5 ->', count <= 5)
    #End of the loop body
#Outside the loop body
print('End of program!')
